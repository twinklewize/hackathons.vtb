  // процент роста с предыдущей недели
  double percentOfIncreasing(String nameOfCompany) {
    Company company =
        companies.firstWhere((comp) => comp.name == nameOfCompany);
    return (company.stonksData.last !=
            company.stonksData[company.stonksData.length - 2])
        ? 100 *
            (company.stonksData.last.open -
                company.stonksData[company.stonksData.length - 2].open) /
            company.stonksData[company.stonksData.length - 2].open
        : 0;
  }

  // перемотка времени для смены стоимости акций хитрым образом
  void rollTime(BuildContext context) {
    for (var company in companies) {
      ChartSampleData oldestData = company.stonksData[0];
      for (var i = 0; i < company.stonksData.length - 1; i++) {
        company.stonksData[i] = company.stonksData[i + 1];
      }
      oldestData.x = Provider.of<Date>(context, listen: false).currentDate;
      company.stonksData.last = oldestData;
    }
    notifyListeners();
  }
